// Primitive data-types
/**
 * Number
 * Boolean
 * String
 * Null
 * Undefined
 * BigInt
 * Symbol
 */

// Example of number data-type
let account_balance = 10;
console.log(account_balance);
console.log(typeof account_balance);

// Example of string data-type
let str = "xyz is a good boy and also he doesn't know how to use zoom";
console.log(str);
console.log(typeof str);

// Example of boolean data-type
let statement = true;
console.log(statement);
console.log(typeof statement);
// Example of  undefined data-type
let account;
console.log(account);
console.log(typeof account);

// Example of null data-type
let balance = null;
console.log(balance);
console.log(typeof balance); // typeof ==> object

// Example of BigInt data-type
let bigIntExample = 12746912891261954832n;
console.log(bigIntExample);
console.log(typeof bigIntExample);
